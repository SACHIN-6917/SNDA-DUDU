from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static

from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('', include('dudu.urls')),  # Include dudu app URLs
    path('accounts/', include('allauth.urls')), # Allauth for SSO
    path('api/token/', TokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),
]

if settings.DEBUG:
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
    # Also include the STATICFILES_DIRS if needed, but STATIC_ROOT is usually enough for runserver
    # Actually, runserver pulls from STATICFILES_DIRS automatically if configured.
    # To be safe for all environments:
    urlpatterns += static('/static/', document_root=settings.BASE_DIR / 'static')
